import * as React from "react"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const badgeVariants = cva(
  "cyber-badge inline-flex items-center rounded-full border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default:
          "border-transparent bg-cyber-cyan/20 text-cyber-cyan hover:bg-cyber-cyan/30",
        secondary:
          "border-transparent bg-cyber-purple/20 text-cyber-purple hover:bg-cyber-purple/30",
        destructive:
          "border-transparent bg-cyber-pink/20 text-cyber-pink hover:bg-cyber-pink/30",
        outline: "text-cyber-cyan border-cyber-cyan/30",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
)

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {}

function Badge({ className, variant, ...props }: BadgeProps) {
  return (
    <div className={cn(badgeVariants({ variant }), className)} {...props} />
  )
}

export { Badge, badgeVariants }
